package datatype;

public class DataTypeDemo7 {

	public static void main(String[] args) {
		// 변수 활용하기
		/*
		 * 본인의 이름, 전화번호, 이메일, 나이, 키, 몸무게, 혈액형, 결혼여부를 
		 * 저장하는 변수를 선언하고 해당 값을 각 변수에 저장하기
		 * 
		 *  각 변수에 저장된 값을 출력하기
		 */
		String 이름 = "권민석";
		String 전화번호 = "010-7942-4465";
		String 이메일 = "siwol_smuire89@naver.com";
		int 나이 = 32;
		double 키 = 168.0;
		double 몸무게 = 95.0;
		String 혈액형 = "A";
		boolean 결혼여부 = false;
		
		System.out.println("이름: "+이름);
		System.out.println("전화번호: "+전화번호);
		System.out.println("이메일: "+이메일);
		System.out.println("나이: "+나이);
		System.out.println("키: "+키);
		System.out.println("몸무게: "+몸무게);
		System.out.println("혈액형: "+혈액형);
		System.out.println("결혼여부: "+결혼여부);
		
	}
}
